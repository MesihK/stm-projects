int16_t _sin(uint16_t angle) { 
	if (angle >= 360) angle = angle % 360; 
	if (angle < 90) return dsin[angle];
	else if (angle < 180) return dsin[180 - angle];
	else if (angle < 270) return -1*dsin[angle - 180];
	else return -1*dsin[360 - angle];
} 
